/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

	/* Initialise game parameters and flags */
//	uint8_t string_to_send1[STR_BUFFER] = "This is a string !\r\n";
//	uint8_t string_to_send2[STR_BUFFER] = "This is a string !\r\n";

	uint8_t sequence[STR_BUFFER] = "CCGGAAGFFEEDDC";

	uint8_t seq_index = 0;
	uint16_t valid_key_index = 0;

	uint16_t seq_complete = 0;

	/* Initialise serial */
	SerialInitialise(BAUD_115200, &USART1_PORT, 0x00);
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_I2C1_Init();
  MX_SPI1_Init();
  MX_USB_PCD_Init();
  MX_TSC_Init();
  /* USER CODE BEGIN 2 */

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */
	  /* Discharge sampling capacitors to read new acquisition value */
	  HAL_TSC_IODischarge(&htsc, ENABLE);
	  HAL_Delay(DELAY_TIME);

	  if (HAL_TSC_Start(&htsc) != HAL_OK) {
		  Error_Handler();
	  }

	  uint16_t uhTSCAcquisitionValueG6 = 0;
	  uint16_t uhTSCAcquisitionValueG8 = 0;

	  while (HAL_TSC_GetState(&htsc) == HAL_TSC_STATE_BUSY) {}

	  /* Get acquisition values from Group 6 */
	  if (HAL_TSC_GroupGetStatus(&htsc, TSC_GROUP6_IDX) == TSC_GROUP_COMPLETED) {
		  /* Store the acquisition value */
	  	  uhTSCAcquisitionValueG6 = (uint16_t)HAL_TSC_GroupGetValue(&htsc, TSC_GROUP6_IDX);

//	  	  sprintf(string_to_send1, "%hu\r\n", uhTSCAcquisitionValueG6);
//	  	  SerialOutputString(string_to_send1, &USART1_PORT);
	  }

	  /* Get acquisition values from Group 8 */
	  if (HAL_TSC_GroupGetStatus(&htsc, TSC_GROUP8_IDX) == TSC_GROUP_COMPLETED) {
		  /* Store the acquisition value */
	  	  uhTSCAcquisitionValueG8 = (uint16_t)HAL_TSC_GroupGetValue(&htsc, TSC_GROUP8_IDX);

//	  	 sprintf(string_to_send2, "%hu\r\n", uhTSCAcquisitionValueG8);
//	  	 SerialOutputString(string_to_send2, &USART1_PORT);
	  }

	  uint8_t key = '_';

	  /* Check if full sequence has been played */
	  if ( seq_complete == 1 ) {
		  /* Send victory character through serial */
		  key = 'W';
		  SerialOutputChar(key, &USART1_PORT);
		  exit(0);
	  }

	  else {
		  /* Check note value from Group 6 acquisition values */
		  /* C, D, E keys */
		  if ( uhTSCAcquisitionValueG8 > G8_NO_NOTE ) {
			  /* Increment valid key press if note is in range of keys */
			  /* C key range */
	  		  if ( (uhTSCAcquisitionValueG6 < C_UPPER) && (uhTSCAcquisitionValueG6 > C_LOWER) ) {
	  			  key = 'C';
	  			  valid_key_index++;
	  		  }
	  		  /* D key range */
	  		  else if ( (uhTSCAcquisitionValueG6 < D_UPPER) && (uhTSCAcquisitionValueG6 > D_LOWER) ) {
	  			  key = 'D';
	  			  valid_key_index++;
	  		  }
	  		  //* E key range */
	  		  else if ( (uhTSCAcquisitionValueG6 < E_UPPER) && (uhTSCAcquisitionValueG6 > E_LOWER) ) {
 				  key = 'E';
 				  valid_key_index++;
 			  }
  		  }
		  /* Check note value from Group 8 acquisition values */
		  /* F, G, A keys */
		  else if ( uhTSCAcquisitionValueG6 > G6_NO_NOTE ) {
			  /* Increment valid key press if note is in range of keys */
			  /* F key range */
	  		  if ( (uhTSCAcquisitionValueG8 < F_UPPER) && (uhTSCAcquisitionValueG8 > F_LOWER) ) {
	  			  key = 'F';
				  valid_key_index++;
 			  }
	  		  /* G key range */
 			  else if ( (uhTSCAcquisitionValueG8 < G_UPPER) && (uhTSCAcquisitionValueG8 > G_LOWER) ) {
 				  key = 'G';
 				  valid_key_index++;
 			  }
	  		  /* A key range */
 			  else if ( (uhTSCAcquisitionValueG8 < A_UPPER) && (uhTSCAcquisitionValueG8 > A_LOWER) ) {
 				  key = 'A';
 				  valid_key_index++;
 			  }
 		  }
	  }

	  /* Check if key has been pressed for a sufficient amount of time */
	  if ( valid_key_index > VALID_KEY_PRESS ) {
		  SerialOutputChar(key, &USART1_PORT);

		  /* If key is correct in sequence increment or reset otherwise */
		  if ( sequence[seq_index] == key ) {
			  seq_index++;
		  }
		  else {
			  seq_index = 0;
		  }

	 	  valid_key_index = 0;
	  }

	  /* If the end of sequence has been reached, raise completion flag */
	  if ( seq_index == strlen(sequence) ) {
		  seq_complete = 1;
	  }
    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

